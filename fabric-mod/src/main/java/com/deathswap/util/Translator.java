/**
 * Central English-to-Chinese (中文) lookup for every user-facing string in the
 * mod — the {@code announce(...)} broadcast verbs and per-effect chat/title
 * lines in {@link com.deathswap.items.ItemRegistry} /
 * {@link com.deathswap.items.ItemManager}, the game-flow titles, deaths and
 * countdown text in {@link com.deathswap.game.Messages}, the scoreboard
 * labels, and the per-item display names shown on the offered dyes. Chinese
 * strings are taken verbatim from the original datapack
 * ({@code Lang Core} 2) where it provided one, and authored to match the same
 * wording/tone where it did not.
 *
 * <p>Lookups are keyed on the exact English string used at the call site, so the
 * English literal stays the source of truth and {@link #translate} falls back to
 * it when no translation exists. Interpolated messages (those with a player name
 * or number spliced into the middle) translate their literal fragments here and
 * keep the concatenation at the call site.
 */

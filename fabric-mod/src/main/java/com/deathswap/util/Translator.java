    private Translator() {
    }

    /** Returns the Chinese string for {@code en} when {@code zh}, else {@code en}. */
    public static String translate(boolean zh, String en) {
        return zh ? ZH.getOrDefault(en, "Failed to translate to ZH: " + en) : en;
    }

